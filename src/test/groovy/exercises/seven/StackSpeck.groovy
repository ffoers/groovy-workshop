package exercises.seven

import spock.lang.Specification

class StackSpeck extends Specification {

    //A Stack has the following methods
    //
    // void push(thing)
    // - after push, the thing lies on top of the stack
    // - it is not allowed to push null
    //
    // def pop()
    // - pop() removes and returns the topmost thing on the stack
    // - pop() on an empty stack throws an StackEmptyException
    //
    // def peek()
    // - returns the topmost thing on the stack without removing it
    //
    // int size()
    // - returns the amount of things on the stack
    //
    // boolean empty()
    // - tests, if the stack is empty


}
